package com.bcj.corejava.Operators.lab5;

import java.util.Scanner;

public class ThreeIntegerArithmatics {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter three integers :");
		int x= sc.nextInt();
		int great=x,small=x;
		int y= sc.nextInt();
		if(y>great)
		  great=y;
		else small=y;
		int z= sc.nextInt();
		if(z>great)
			great=z;
		else  small=z;
		
System.out.println("sum is :"+(x+y+z));
System.out.println("product is :"+(x*y*z));
System.out.println("Average is :"+((x+y+z)/3));
System.out.println("Biggest of three is "+great );
System.out.println("smallest of three is "+small);
	}

}
