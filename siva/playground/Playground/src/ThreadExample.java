
public class ThreadExample extends Thread {
	public void run() {
		char[] ltrs = { 'A', 'B', 'C', 'D', 'E' };

		for (int i = 0; i < ltrs.length; i++) {
			System.out.print(" " + ltrs[i]);

		}

	}
}

