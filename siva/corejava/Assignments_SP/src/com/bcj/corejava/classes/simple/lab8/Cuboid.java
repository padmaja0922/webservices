package com.bcj.corejava.classes.simple.lab8;

public class Cuboid {
	private  int  length;
	private int width;
	private int height;
	Cuboid(int length,int width,int height){
		this.length=length;
		this.width=width;
		this.height=height;		
	}
	public int getLength() {
		return length;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;

}}
