package com.bcj.psbregent.tenantportal.entity;


/**
 * @author Boot Camp User 008
 *
 */
public class Company {
	
	private String cName ;
	private String cEmail;
	private String cPhone;
	private String userId;
	private String password;
	Address address;
	Tenant tent;
	
	
	
	/**
	 *  Overriding toString method
	 */
	 
     public String toString(){
		
		return (this.cName +"  "+this.cEmail + "  "+ this.cPhone +"  "+ this.userId+"  "+ this.password +" "+this.address
				 +"  "+ this.tent);
	}
	
	
	public Company() {
		super();
	}
	/**
	 * @param cName
	 * @param cEmail
	 * @param cPhone
	 * @param userId
	 * @param password
	 * @param address
	 * @param tent
	 */
	public Company(String cName, String cEmail, String cPhone, String userId, String password, Address address,
			Tenant tent) {
		super();
		this.cName = cName;
		this.cEmail = cEmail;
		this.cPhone = cPhone;
		this.userId = userId;
		this.password = password;
		this.address = address;
		this.tent = tent;
	}
	/**
	 * @return the cName
	 */
	public String getcName() {
		return cName;
	}
	/**
	 * @param cName the cName to set
	 */
	public void setcName(String cName) {
		this.cName = cName;
	}
	/**
	 * @return the cEmail
	 */
	public String getcEmail() {
		return cEmail;
	}
	/**
	 * @param cEmail the cEmail to set
	 */
	public void setcEmail(String cEmail) {
		this.cEmail = cEmail;
	}
	/**
	 * @return the cPhone
	 */
	public String getcPhone() {
		return cPhone;
	}
	/**
	 * @param cPhone the cPhone to set
	 */
	public void setcPhone(String cPhone) {
		this.cPhone = cPhone;
	}
	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}
	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	/**
	 * @return the address
	 */
	public Address getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(Address address) {
		this.address = address;
	}
	/**
	 * @return the tent
	 */
	public Tenant getTent() {
		return tent;
	}
	/**
	 * @param tent the tent to set
	 */
	public void setTent(Tenant tent) {
		this.tent = tent;
	}
	
	

}
