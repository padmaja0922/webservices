package com.bcj.corejava.classes.medium.lab6;

public class IntegerWapper {

	  private int i;
	  public IntegerWapper(int i)
	  { this.i = i; }
	
	
	
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
public static void swap(IntegerWapper iw,IntegerWapper iw1)

{
	System.out.println("before swapping"+"\n"+iw.getI()+"  "+iw1.getI());
	
	int temp=iw.getI();
	iw.setI(iw1.getI());//swapping the objects by getters
	iw1.setI(temp);
	System.out.println("after swapping"+"\n"+iw.getI()+"  "+iw1.getI());
	
	
}
}
